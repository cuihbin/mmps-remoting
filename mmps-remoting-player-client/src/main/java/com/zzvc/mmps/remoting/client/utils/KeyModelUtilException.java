package com.zzvc.mmps.remoting.client.utils;


public class KeyModelUtilException extends RuntimeException {
	private static final long serialVersionUID = 1L;
	
	public KeyModelUtilException() {
		super();
	}

	public KeyModelUtilException(String message) {
		super(message);
	}
	
	public KeyModelUtilException(Throwable cause) {
		super(cause);
	}
    
    public KeyModelUtilException(String message, Throwable cause) {
    	super(message, cause);
    }
	
}
